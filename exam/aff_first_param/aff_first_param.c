#include <unistd.h>

int	main(int argc, char *argv[])
{
	int	i;
	int j;

	i = 1;
	j = 0;
	if (argc <= 1)
	{
		write(1, "\n", 1);
	}
	if (i < argc)
	{
		while (argv[1][j] != '\0')
		{
			write(1, &argv[1][j], 1);
			j++;
		}
		write(1, "\n", 1);
	}
	return (0);
}
